// Function to generate custom avatar for any user
export const generateCustomAvatar = (firstName, lastName) => {
  // Use first letter of first name to pick a color scheme
  const firstLetter = firstName.charAt(0).toLowerCase();
  const colorSchemes = {
    'a': '264653', // Blue-green
    'b': '8b5cf6', // Purple
    'c': '059669', // Green
    'd': 'f59e0b', // Yellow
    'e': '1e40af', // Blue
    'f': 'dc2626', // Red
    'g': '9333ea', // Purple
    'h': '1f2937', // Gray
    'i': '0f766e', // Teal
    'j': 'be185d', // Pink
    'k': '92400e', // Orange
    'l': '1e3a8a', // Blue
    'm': '7c2d12', // Red
    'n': '581c87', // Purple
    'o': '1f2937', // Gray
    'p': '0f766e', // Teal
    'q': 'be185d', // Pink
    'r': '92400e', // Orange
    's': '1e3a8a', // Blue
    't': '7c2d12', // Red
    'u': '9333ea', // Purple
    'v': '1f2937', // Gray
    'w': '0f766e', // Teal
    'x': 'be185d', // Pink
    'y': '92400e', // Orange
    'z': '1e3a8a'  // Blue
  };
  
  const backgroundColor = colorSchemes[firstLetter] || '6b7280';
  const initials = `${firstName.charAt(0)}${lastName.charAt(0)}`.toUpperCase();
  return `https://ui-avatars.com/api/?name=${initials}&background=${backgroundColor}&color=ffffff&size=120&font-size=0.4&bold=true`;
};
